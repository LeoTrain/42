#include <unistd.h>

void	ft_print_comb2(void)
{
	int		i;
	int		j;
	char	a;

	i = 0;
	while (i <= 98)
	{
		j = i + 1;
		while (j <= 99)
		{
			a = (i / 10) + 48;
			write(1, &a, 1);
			a = (i % 10) + 48;
			write(1, &a, 1);
			write(1, " ", 1);
			a = (j / 10) + 48;
			write(1, &a, 1);
			a = (j % 10) + 48;
			write(1, &a, 1);
			if (!(i == 98 && j == 99))
				write(1, ", ", 1);
			j++;
		}
		i++;
	}
}
